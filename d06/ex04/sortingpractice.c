#include <stdio.h>

int		main()
{
	char	s1[] = "12, 10, 15";
	int		len;
	int 	n;
	char	temp;

	len = 3;
	printf("%s\n", s1);
	while (s1[n] > s1[n+1] && len > 0)
	{
		temp = s1[n];
		s1[n] = s1[n + 1];
		s1[n + 1] = temp;
		n++;
		len--;
	}
	printf("%s", s1);
	return (0);
}
