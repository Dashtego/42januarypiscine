int		ft_iterative_factorial(int nb)
{
	int		i;

	i = nb;
	if (nb > 1 && nb <= 12)
	{
		nb = i * (i - 1);
		while ((i - 1) > 1)
		{
			i--;
			nb = nb * (i - 1);
		}
		return (nb);
	}
	else if (i >= 0 && i <= 1)
		return (1);
	return (0);
}
